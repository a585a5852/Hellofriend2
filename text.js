/*canvas จะต้องใช้ค่า x (width) และ y (height) เพื่อกำหนดตำแหน่งใน canvas

.moveTo กำหนดจุดเริ่มต้นของเส้น
.lineTo กำหนดเส้นที่ลากจากจุดเริ่มต้นไปจุดสุดท้าย
.lineWidth กำหนดความหนาของเส้น
.strokeStyle กำหนดสี
.stroke() function กำหนดให้ทุกอย่างเปลี่ยนสี .strokeStyle
.beginPath() คำสั้งที่จะใช้นการขึ้น Path ใหม่
.closePath() คำสั้งจบการทำงานของ Path
.fillStyle กำหนดสีและแรเงา
.fill() ระบานสี
.fillRect(x, y , width , height) function สร้างสามเหลี่ยม
.strokeRect(x, y , width , height) สามเหลี่ยมเริ่มต้นไม่มีสี
.fillText(message , x , y) function สร้างข้อความตามตำแหน่ง x,y
.strokeText(message , x , y) function สร้างข้อความตามตำแหน่ง x,y เริ่มต้นไม่มีสี 
.font = [normal][italic][mormal | bold] [size] [font name]
.drawImage(image, x,y, width, height) function แสดงรูปภาพนั้นๆตามตำแหน่ง
*/